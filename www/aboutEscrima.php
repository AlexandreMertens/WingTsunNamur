    <h2>L'Escrima</h2>
                  <p>
                    Newman-Escrima est un système de combat avec armes, relativement jeune, logique et dynamique né en 1974 (apparu en Europe dans les années 70) grâce au GM Bill Newman,  à Londres (Angleterre), et ayant ses racines à Stockton (U.S.A.).
                  </p>
                  <p>
Cet art a une longue histoire de violence qui remonte à l'année 1521 lorsque les Philippines ont été conquises par les espagnols. Après la guerre, de nombreux Philippins ont émigré aux États-Unis en prenant « Arnis de Mano » avec eux bien sûr. La plupart d'entre eux sont devenus résidents à Hawaï et à Stockton, en Californie, d'où « Arnis / Escrima » a commencé sa diffusion sur la scène artistique martiale américaine.
                  </p>
                  <p>
La redécouverte d’Escrima est dans une certaine mesure due à Bruce Lee. En démontrant l'utilisation des bâtons Philippins dans ses films "Enter the Dragon" (Opération dragon) et "Jeu de la Mort", il a sensibilisé le public à cet art martial ancien, l’Escrima.
                  </p>
                  <p>
Le Grand Master Bill Newman est considéré comme « Le Père de l’Escrima en Europe ».
                  </p>
                  <p>
Newman-Escrima est un art martial sophistiqué mais simple en même temps, fondé sur des lois physiques simples et est très économique dans les mouvements. L'enseignement sera en conformité avec la devise :
                  </p>
                  <p>
« Less is more »
                  </p>
                  <p>
Les mouvements complexes et acrobatiques seront remplacés par la simplicité et l’efficacité. Tous les mouvements sont naturels et peuvent être appris par n'importe qui.  Il n’est pas nécessaire d’avoir des aptitudes athlétiques ou acrobatiques pour apprendre cet art martial. La seule exigence est de vouloir apprendre quelque chose de nouveau.
                  </p>
                  <p>
La pratique de l'Escrima permet également de mieux comprendre le danger d'une arme blanche et désillusionner les pratiquants qui s’imaginent pouvoir se débarrasser facilement, à mains nues, d'un agresseur armé et expérimenté.
                  </p>
                  <p>
La puissance, l'équilibre, la dextérité, la fluidité, la stabilité (l’ancrage) sont autant d'éléments que le pratiquant va apprendre à développer et maitriser. En effet, l'Escrima repose sur des concepts et stratégies et non sur l'apprentissage de techniques artistiques et acrobatiques.
                  </p>
                  <p>
L'Escrima est aussi un art martial qui permet de se défendre dans toutes les situations d'urgence avec ce qui se trouve sous la main et qui peut devenir une arme improvisée telle que : un stylo, un trousseau de clés, une revue, un bâton de rouge à lèvre, un briquet, un sac à main, une béquille, etc…
                  </p>
                  <p>
Vous devrez toujours garder deux choses à l'esprit :
1.Les attaquants cherchent des victimes, pas des adversaires. Newman-Escrima fera de vous un adversaire.
2.Seules les personnes formées au maniement des armes ont une chance sérieuse contre elles.
                  </p>

